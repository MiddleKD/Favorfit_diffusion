    uncond_prompt=None,
    input_image=None,
    control_image=None,
    do_cfg=True,
    cfg_scale=7.5,
    sampler_name="ddpm",
    n_inference_steps=50,
    strength=0.8,
    models={},
    seed=None,
    device=None,
    idle_device="cpu",
    tokenizer=None,
    dtype=torch.float16,
    leave_tqdm=True,
